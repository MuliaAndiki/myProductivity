const fs = require("fs");
const path = require("path");

const THEME_CONFIG_PATH = path.join(__dirname, "..", "config", "theme.config.ts");
const GLOBAL_CSS_PATH = path.join(__dirname, "..", "styles", "global.css");

function parseThemeConfig(source) {
  const match = source.match(
    /export const themeConfig\s*:\s*ThemeConfig\s*=\s*({[\s\S]*?});/
  );
  if (!match) {
    throw new Error("Could not find `export const themeConfig = { ... }` in theme.config.ts");
  }

   
  return new Function(`return (${match[1]});`)();
}

function parseHexToRgb(hex) {
  const value = hex.replace("#", "").trim();
  if (value.length === 3) {
    const r = parseInt(value[0] + value[0], 16);
    const g = parseInt(value[1] + value[1], 16);
    const b = parseInt(value[2] + value[2], 16);
    return `${r} ${g} ${b}`;
  }
  if (value.length === 6 || value.length === 8) {
    const r = parseInt(value.slice(0, 2), 16);
    const g = parseInt(value.slice(2, 4), 16);
    const b = parseInt(value.slice(4, 6), 16);
    return `${r} ${g} ${b}`;
  }
  throw new Error(`Unsupported hex color: ${hex}`);
}

function parseRgbToRgb(value) {
  const match = value.match(/rgba?\(([^)]+)\)/i);
  const raw = match ? match[1] : value;
  const cleaned = raw.replace(/[rgba()\/]/gi, " ").replace(/\s+/g, " ").trim();
  const parts = cleaned
    .split(/[, ]+/)
    .map((part) => part.trim())
    .filter(Boolean);
  if (parts.length < 3) {
    throw new Error(`Invalid rgb/rgba color: ${value}`);
  }
  const [r, g, b] = parts;
  return `${Number(r)} ${Number(g)} ${Number(b)}`;
}

function toRgb(value) {
  if (typeof value !== "string") {
    throw new Error(`Color value must be string, got: ${typeof value}`);
  }
  const normalized = value.trim().toLowerCase();
  if (normalized.startsWith("#")) {
    return parseHexToRgb(normalized);
  }
  if (normalized.startsWith("rgb")) {
    return parseRgbToRgb(normalized);
  }
  throw new Error(`Unsupported color format: ${value}`);
}

