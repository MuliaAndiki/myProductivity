function main() {
  const source = fs.readFileSync(THEME_CONFIG_PATH, "utf8");
  const themeConfig = parseThemeConfig(source);
  const css = buildCss(themeConfig);
  fs.writeFileSync(GLOBAL_CSS_PATH, css, "utf8");
  console.log("Generated:", path.relative(process.cwd(), GLOBAL_CSS_PATH));
}

main();
